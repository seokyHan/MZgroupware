package com.mzgw.dto;

public class WorkMemVO {

	private int jobCode;
	private String jobMemCode;
	private String empId;
	private String empName;
	private String empDept;
	private String empRank;
	
	public int getJobCode() {
		return jobCode;
	}
	
	public void setJobCode(int jobCode) {
		this.jobCode = jobCode;
	}
	
	public String getJobMemCode() {
		return jobMemCode;
	}
	
	public void setJobMemCode(String jobMemCode) {
		this.jobMemCode = jobMemCode;
	}

	public String getEmpName() {
		return empName;
	}

	public void setEmpName(String empName) {
		this.empName = empName;
	}

	public String getEmpDept() {
		return empDept;
	}

	public void setEmpDept(String empDept) {
		this.empDept = empDept;
	}

	public String getEmpRank() {
		return empRank;
	}

	public void setEmpRank(String empRank) {
		this.empRank = empRank;
	}

	public String getEmpId() {
		return empId;
	}

	public void setEmpId(String empId) {
		this.empId = empId;
	}
	
	
	
	
	
	
}
