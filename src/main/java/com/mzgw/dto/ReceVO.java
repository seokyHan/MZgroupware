package com.mzgw.dto;

public class ReceVO {
	
	private int emailNo;
	private String id;
	private String ncnm;
	private String receEmp;
	private String receName;
	private String receEmail;
	private String receNcnm;
	
	private String tableCode = "R";
	
	public int getEmailNo() {
		return emailNo;
	}
	
	public void setEmailNo(int emailNo) {
		this.emailNo = emailNo;
	}
	
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNcnm() {
		return ncnm;
	}

	public void setNcnm(String ncnm) {
		this.ncnm = ncnm;
	}

	public String getReceEmp() {
		return receEmp;
	}
	
	public void setReceEmp(String receEmp) {
		this.receEmp = receEmp;
	}
	
	public String getReceEmail() {
		return receEmail;
	}
	
	public void setReceEmail(String receEmail) {
		this.receEmail = receEmail;
	}

	public String getReceName() {
		return receName;
	}

	public void setReceName(String receName) {
		this.receName = receName;
	}

	public String getReceNcnm() {
		return receNcnm;
	}

	public void setReceNcnm(String receNcnm) {
		this.receNcnm = receNcnm;
	}

	public String getTableCode() {
		return tableCode;
	}

	public void setTableCode(String tableCode) {
		this.tableCode = tableCode;
	}
	
	
	
	
	
}
