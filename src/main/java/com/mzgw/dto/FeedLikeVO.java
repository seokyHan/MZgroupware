package com.mzgw.dto;

public class FeedLikeVO {

	private int likeNo;
	private String feedEmp;
	private String likeEmp;
	private int likeCheck;
	
	public int getLikeNo() {
		return likeNo;
	}
	
	public void setLikeNo(int likeNo) {
		this.likeNo = likeNo;
	}
	
	public String getFeedEmp() {
		return feedEmp;
	}
	
	public void setFeedEmp(String feedEmp) {
		this.feedEmp = feedEmp;
	}
	
	public String getLikeEmp() {
		return likeEmp;
	}
	
	public void setLikeEmp(String likeEmp) {
		this.likeEmp = likeEmp;
	}
	
	public int getLikeCheck() {
		return likeCheck;
	}
	
	public void setLikeCheck(int likeCheck) {
		this.likeCheck = likeCheck;
	}
	
	
	
	
}
