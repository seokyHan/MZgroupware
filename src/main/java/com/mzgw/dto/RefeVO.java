package com.mzgw.dto;

public class RefeVO {
	
	private int emailNo;
	private String refeEmp;
	private String refeName;
	private String refeEmail;
	
	public int getEmailNo() {
		return emailNo;
	}
	
	public void setEmailNo(int emailNo) {
		this.emailNo = emailNo;
	}
	
	public String getRefeEmp() {
		return refeEmp;
	}
	
	public void setRefeEmp(String refeEmp) {
		this.refeEmp = refeEmp;
	}
	
	public String getRefeEmail() {
		return refeEmail;
	}
	
	public void setRefeEmail(String refeEmail) {
		this.refeEmail = refeEmail;
	}

	public String getRefeName() {
		return refeName;
	}

	public void setRefeName(String refeName) {
		this.refeName = refeName;
	}
	
	
	
	
	
}
